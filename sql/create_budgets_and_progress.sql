create table if not exists public.budgets (
  id bigint generated by default as identity primary key,
  workspace_id bigint not null references public.workspaces(id) on delete cascade,
  created_by_user_id uuid null,
  updated_by_user_id uuid null,
  name text not null,
  period_start date not null,
  period_end date not null,
  currency_code char(3) not null,
  category_id bigint null references public.categories(id) on delete set null,
  account_id bigint null references public.accounts(id) on delete set null,
  limit_amount numeric(14,2) not null,
  rollover_enabled boolean not null default false,
  alert_percent numeric(5,2) not null default 80,
  notes text null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint budgets_name_not_blank_chk check (btrim(name) <> ''),
  constraint budgets_period_order_chk check (period_end >= period_start),
  constraint budgets_limit_amount_positive_chk check (limit_amount > 0),
  constraint budgets_alert_percent_range_chk check (alert_percent >= 0 and alert_percent <= 100),
  constraint budgets_currency_code_chk check (char_length(btrim(currency_code::text)) = 3)
);

comment on table public.budgets is
  'Presupuestos por periodo. Pueden ser generales, por categoria, por cuenta o por categoria dentro de una cuenta segun category_id y account_id.';

comment on column public.budgets.limit_amount is
  'Monto maximo permitido dentro del periodo.';

comment on column public.budgets.alert_percent is
  'Porcentaje del limite desde el cual el presupuesto entra en alerta.';

create index if not exists budgets_workspace_period_idx
  on public.budgets (workspace_id, period_start, period_end);

create index if not exists budgets_workspace_category_idx
  on public.budgets (workspace_id, category_id, period_start, period_end);

create index if not exists budgets_workspace_account_idx
  on public.budgets (workspace_id, account_id, period_start, period_end);

create index if not exists budgets_workspace_active_idx
  on public.budgets (workspace_id, is_active, period_end desc);

create or replace function public.trg_budgets_normalize_and_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.currency_code := upper(btrim(new.currency_code));
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists trg_budgets_normalize_and_touch_updated_at on public.budgets;

create trigger trg_budgets_normalize_and_touch_updated_at
before insert or update on public.budgets
for each row
execute function public.trg_budgets_normalize_and_touch_updated_at();

alter table public.budgets enable row level security;

drop policy if exists budgets_select_workspace_members on public.budgets;
create policy budgets_select_workspace_members
  on public.budgets
  for select
  to authenticated
  using (
    exists (
      select 1
      from public.workspace_members wm
      where wm.workspace_id = budgets.workspace_id
        and wm.user_id = auth.uid()
    )
  );

drop policy if exists budgets_insert_workspace_members on public.budgets;
create policy budgets_insert_workspace_members
  on public.budgets
  for insert
  to authenticated
  with check (
    exists (
      select 1
      from public.workspace_members wm
      where wm.workspace_id = budgets.workspace_id
        and wm.user_id = auth.uid()
    )
    and (created_by_user_id is null or created_by_user_id = auth.uid())
    and (updated_by_user_id is null or updated_by_user_id = auth.uid())
  );

drop policy if exists budgets_update_workspace_members on public.budgets;

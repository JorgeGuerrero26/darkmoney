create or replace function public.trg_workspace_invitations_normalize_and_touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.invited_email := lower(btrim(new.invited_email));
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists trg_workspace_invitations_normalize_and_touch_updated_at on public.workspace_invitations;

create trigger trg_workspace_invitations_normalize_and_touch_updated_at
before insert or update on public.workspace_invitations
for each row
execute function public.trg_workspace_invitations_normalize_and_touch_updated_at();

create or replace function public.find_darkmoney_user_by_email(lookup_email text)
returns table (
  user_id uuid,
  email text,
  full_name text
)
language sql
security definer
set search_path = public, auth
as $$
  select
    u.id as user_id,
    lower(u.email) as email,
    coalesce(nullif(btrim(p.full_name), ''), split_part(u.email, '@', 1)) as full_name
  from auth.users u
  left join public.profiles p
    on p.id = u.id
  where lower(u.email) = lower(btrim(lookup_email))
  limit 1
$$;

comment on function public.find_darkmoney_user_by_email(text) is
  'Busca un usuario real de DarkMoney por email para invitaciones colaborativas y registros compartidos.';

revoke all on function public.find_darkmoney_user_by_email(text) from public;
grant execute on function public.find_darkmoney_user_by_email(text) to service_role;

create or replace function public.find_darkmoney_users_by_ids(lookup_user_ids uuid[])
returns table (
  user_id uuid,
  email text,
  full_name text
)
language sql
security definer
set search_path = public, auth
as $$
  select
    u.id as user_id,
    lower(u.email) as email,
    coalesce(nullif(btrim(p.full_name), ''), split_part(u.email, '@', 1)) as full_name
  from auth.users u
  left join public.profiles p
    on p.id = u.id
  where u.id = any(coalesce(lookup_user_ids, array[]::uuid[]))
$$;

comment on function public.find_darkmoney_users_by_ids(uuid[]) is
  'Devuelve usuarios reales de DarkMoney por lista de ids para flujos colaborativos.';

revoke all on function public.find_darkmoney_users_by_ids(uuid[]) from public;
grant execute on function public.find_darkmoney_users_by_ids(uuid[]) to service_role;

alter table public.workspace_invitations enable row level security;

drop policy if exists workspace_invitations_select_workspace_members on public.workspace_invitations;
create policy workspace_invitations_select_workspace_members
  on public.workspace_invitations
  for select
  to authenticated
  using (
    invited_user_id = auth.uid()
    or exists (
      select 1
      from public.workspace_members wm
      where wm.workspace_id = workspace_invitations.workspace_id
        and wm.user_id = auth.uid()
    )
  );

drop policy if exists workspace_invitations_service_role_all on public.workspace_invitations;
create policy workspace_invitations_service_role_all
  on public.workspace_invitations
  for all
  to service_role
  using (true)
  with check (true);

grant select on public.workspace_invitations to authenticated;
grant all on public.workspace_invitations to service_role;
grant usage, select on sequence public.workspace_invitations_id_seq to service_role;

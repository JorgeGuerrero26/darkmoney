create table if not exists public.workspace_invitations (
  id bigint generated by default as identity primary key,
  workspace_id bigint not null references public.workspaces(id) on delete cascade,
  invited_by_user_id uuid not null references auth.users(id) on delete restrict,
  invited_user_id uuid not null references auth.users(id) on delete restrict,
  invited_email text not null,
  invited_display_name text null,
  invited_by_display_name text null,
  role public.workspace_role not null default 'member',
  status public.workspace_invitation_status not null default 'pending',
  token uuid not null default gen_random_uuid(),
  note text null,
  accepted_at timestamptz null,
  responded_at timestamptz null,
  last_sent_at timestamptz null,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint workspace_invitations_invited_email_not_blank_chk check (btrim(invited_email) <> ''),
  constraint workspace_invitations_metadata_object_chk check (jsonb_typeof(metadata) = 'object')
);

